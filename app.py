@app.route("/chat", methods=["POST"])
def chat():
    data = request.get_json()
    scenario_id = data.get("scenario_id")
    messages = data.get("messages", [])
    lang = data.get("lang", "tj")

    scenario = next((s for s in SCENARIOS if s["id"] == scenario_id), None)
    if not scenario:
        return jsonify({"error": "Сценарий ёфт нашуд"}), 404

    if lang == "tj":
        title = scenario["title"]
        desc = scenario["desc"]
        system = f"""Ту як симулятори иҷтимоӣ ҳастӣ. Нақш: {title}.
Вазъият: {desc}

Қоидаҳо:
1. Ба тоҷикӣ гап зан
2. Нақши худро хуб бозӣ кун — табиӣ бош
3. Вақте ки сценарий табиӣ тамом шуд, ТАНҲО ин сатрро бинавис: [СУҲБАТ_ТАМОМ]
4. Ҷавобҳоят кӯтоҳ ва табиӣ бошанд (1-3 ҷумла)
5. Аз характери худ берун нашав"""
    else:
        title = scenario["title_ru"]
        desc = scenario["desc_ru"]
        system = f"""Ты симулятор социальных навыков. Роль: {title}.
Ситуация: {desc}

Правила:
1. Говори по-русски
2. Хорошо играй свою роль — будь естественным
3. Когда сценарий естественно завершится, напиши ТОЛЬКО эту строку: [СУҲБАТ_ТАМОМ]
4. Ответы короткие и естественные (1-3 предложения)
5. Не выходи из роли"""

    groq_messages = [{"role": "system", "content": system}] + messages

    try:
        completion = groq_client.chat.completions.create(
            model="llama-3.1-8b-instant",
            messages=groq_messages,
            max_tokens=300,
            temperature=0.85
        )
        reply = completion.choices[0].message.content
        is_done = "[СУҲБАТ_ТАМОМ]" in reply
        reply_clean = reply.replace("[СУҲБАТ_ТАМОМ]", "").strip()
        return jsonify({"reply": reply_clean, "done": is_done})
    except Exception as e:
        return jsonify({"error": str(e)}), 500

